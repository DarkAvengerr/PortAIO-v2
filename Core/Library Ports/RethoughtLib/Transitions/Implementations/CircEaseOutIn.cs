using EloBuddy; 
using LeagueSharp.Common; 
namespace RethoughtLib.Transitions.Implementations
{
    #region Using Directives

    using System;

    using RethoughtLib.Transitions.Abstract_Base;

    #endregion

    /// <summary>
    ///     The circular ease out in.
    /// </summary>
    public class CircEaseOutIn : TransitionBase
    {
        #region Constructors and Destructors

        #region Constructors

        /// <summary>
        ///     Initializes a new instance of the <see cref="CircEaseOutIn" /> class.
        /// </summary>
        /// <param name="duration">
        ///     The duration.
        /// </param>
        public CircEaseOutIn(double duration)
            : base(duration)
        {
        }

        #endregion

        #endregion

        #region Public Methods and Operators

        /// <summary>
        ///     The circular ease in.
        /// </summary>
        /// <param name="t">
        ///     The t.
        /// </param>
        /// <param name="b">
        ///     The b.
        /// </param>
        /// <param name="c">
        ///     The c.
        /// </param>
        /// <param name="d">
        ///     The d.
        /// </param>
        /// <returns>
        ///     The <see cref="double" />.
        /// </returns>
        public static double CircEaseIn(double t, double b, double c, double d)
        {
            return -c * (Math.Sqrt(1 - (t /= d) * t) - 1) + b;
        }

        /// <summary>
        ///     The circular ease out.
        /// </summary>
        /// <param name="t">
        ///     The t.
        /// </param>
        /// <param name="b">
        ///     The b.
        /// </param>
        /// <param name="c">
        ///     The c.
        /// </param>
        /// <param name="d">
        ///     The d.
        /// </param>
        /// <returns>
        ///     The <see cref="double" />.
        /// </returns>
        public static double CircEaseOut(double t, double b, double c, double d)
        {
            return c * Math.Sqrt(1 - (t = t / d - 1) * t) + b;
        }

        /// <summary>
        ///     The equation.
        /// </summary>
        /// <param name="time">
        ///     The t.
        /// </param>
        /// <param name="b">
        ///     The b.
        /// </param>
        /// <param name="c">
        ///     The c.
        /// </param>
        /// <param name="startTime">
        ///     The d.
        /// </param>
        /// <returns>
        ///     The <see cref="double" />.
        /// </returns>
        public override double Equation(double time, double b, double c, double startTime)
        {
            if (time < startTime / 2) return CircEaseOut(time * 2, b, c / 2, startTime);

            return CircEaseIn(time * 2 - startTime, b + c / 2, c / 2, startTime);
        }

        #endregion
    }
}